        static int getPosition(byte[]data, byte[] pattern, uint pattern_size)
        {
            uint matched_idx = 0U;
            int position = -1;

            for (uint i = 0; i < data.Length; i++)
            {
                if (data[i] == pattern[matched_idx])
                {
                    matched_idx++;
                    if (pattern_size == matched_idx)
                    {
                        position = ((int)i - (int)pattern_size) + 1;
                        break;
                    }
                }
                else
                {
                    matched_idx = 0U;
                }
            }
            return position;
        }

        static FlashPattern? getFlashPattern(byte[] data)
        {
            int position;

            FlashType_e flashtype = FlashType_e.UNKNOWN;
            FlashPattern? pattern = null;
            for (int i = 0; i < FlashPatterns.Length; i++)
            {
                position = getPosition(data, FlashPatterns[i].IdentPattern, (uint)FlashPatterns[i].IdentPattern.Length);
                if (position > 0)
                {
                    pattern = FlashPatterns[i];
                    flashtype = pattern.FlashType;
                    break;
                }
            }
            return pattern;
        }

        static int patch(byte[] data, FlashPattern pattern)
        {
            int retVal = 0;
            for (uint i = 0; i < pattern.PatchCount; i++)
            {
                int position = -1;
                position = getPosition(data, pattern.Patches[i].Marker, (uint)pattern.Patches[i].Marker.Length);
                if (position > 0)
                {
                    Buffer.BlockCopy(pattern.Patches[i].Replace, 0, data, position, pattern.Patches[i].Replace.Length);
                }
                else
                {
                    Console.WriteLine("ERROR: didn't find pattern #%d\n", i);
                    retVal = -1;
                }
            }

            return retVal;
        }
        public static byte[] ApplySRAMPatch(
                    byte[] rom)
        {
            if (rom is null || rom.Length < 0x200)
            {
                String error = "[ERR] Invalid ROM or too short.";
                Console.WriteLine(error);
                throw new ArgumentException(error);
                
            }
            byte[] outRom = rom;



            FlashPattern? pattern = getFlashPattern(outRom);
            

            if (pattern == null)
            {
                String error = "[ERR] Error: Flash Type not detected.";
                Console.WriteLine(error);
                throw new ArgumentException(error);
            }
            else
            {

                Console.WriteLine($"[INFO] SRAM patching {nameof(pattern.FlashType)}");

                if (0 == patch(outRom, pattern))
                {
                    Console.WriteLine("[INFO] SRAM Patching done.");
                }
                else
                {
                    Console.WriteLine("[INFO] Error - file could not be patched. Has it been patches before?");
                }
            }

            return outRom;
        }
